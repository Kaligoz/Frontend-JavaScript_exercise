let firstNumber = prompt("Введіть перше число:");
let secondNumber = prompt("Введіть друге число:");

let plus = Number(firstNumber) + Number(secondNumber);
alert(`Сума двох чисел: ${plus}`);

let minus = Number(firstNumber) - Number(secondNumber);
alert(`Різниця двох чисел: ${minus}`);

let multiply = Number(firstNumber) * Number(secondNumber);
alert(`Добуток двох чисел: ${multiply}`);

let divide = Number(firstNumber) / Number(secondNumber);
alert(`Ділення двох чисел: ${divide}`);